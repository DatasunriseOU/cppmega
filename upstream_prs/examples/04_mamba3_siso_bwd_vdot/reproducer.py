def bench(kernel_obj, label):
    install_kernel(kernel_obj)
    # Fresh input set each time so saved_tensors are aligned with the swap
    tensors, grad_out = make_inputs(requires_grad=True)

    # Warmup (covers autotune)
    for _ in range(args.warmup):
        for t in tensors.values():
            if isinstance(t, torch.Tensor) and t.grad is not None:
                t.grad = None
        out = mamba3_siso_combined(
            tensors["Q"], tensors["K"], tensors["V"],
            tensors["ADT"], tensors["DT"], tensors["Trap"],
            tensors["Q_bias"], tensors["K_bias"], tensors["Angles"],
            D=tensors["D"], Z=tensors["Z"], Input_States=None,
            chunk_size=args.chunk_size, return_final_states=False, cu_seqlens=None,
        )
        out.backward(grad_out, retain_graph=False)
    torch.cuda.synchronize()

    # Timed loop - we re-run full fwd+bwd each iter because saved_tensors are
    # consumed on backward; then subtract an fwd-only baseline measured the
    # same way.
    start = [torch.cuda.Event(enable_timing=True) for _ in range(args.iters)]
    end = [torch.cuda.Event(enable_timing=True) for _ in range(args.iters)]
    fb_times = []
    for i in range(args.iters):
        for t in tensors.values():
            if isinstance(t, torch.Tensor) and t.grad is not None:
                t.grad = None
        start[i].record()
        out = mamba3_siso_combined(
            tensors["Q"], tensors["K"], tensors["V"],
            tensors["ADT"], tensors["DT"], tensors["Trap"],
            tensors["Q_bias"], tensors["K_bias"], tensors["Angles"],
            D=tensors["D"], Z=tensors["Z"], Input_States=None,
            chunk_size=args.chunk_size, return_final_states=False, cu_seqlens=None,
        )
        out.backward(grad_out, retain_graph=False)
        end[i].record()
    torch.cuda.synchronize()
    fb_times = [s.elapsed_time(e) for s, e in zip(start, end)]

    # Forward-only baseline with same inputs / no grad (for bwd isolation)
    tensors_nograd, grad_out_ng = make_inputs(requires_grad=False)
    fwd_times = []
    for _ in range(args.warmup):
        _ = mamba3_siso_combined(
            tensors_nograd["Q"], tensors_nograd["K"], tensors_nograd["V"],
            tensors_nograd["ADT"], tensors_nograd["DT"], tensors_nograd["Trap"],
            tensors_nograd["Q_bias"], tensors_nograd["K_bias"], tensors_nograd["Angles"],
            D=tensors_nograd["D"], Z=tensors_nograd["Z"], Input_States=None,
            chunk_size=args.chunk_size, return_final_states=False, cu_seqlens=None,
        )
    torch.cuda.synchronize()
    for i in range(args.iters):
        s = torch.cuda.Event(enable_timing=True); e = torch.cuda.Event(enable_timing=True)
        s.record()
        _ = mamba3_siso_combined(
            tensors_nograd["Q"], tensors_nograd["K"], tensors_nograd["V"],
            tensors_nograd["ADT"], tensors_nograd["DT"], tensors_nograd["Trap"],
            tensors_nograd["Q_bias"], tensors_nograd["K_bias"], tensors_nograd["Angles"],
            D=tensors_nograd["D"], Z=tensors_nograd["Z"], Input_States=None,
            chunk_size=args.chunk_size, return_final_states=False, cu_seqlens=None,
        )
        e.record()
        torch.cuda.synchronize()
        fwd_times.append(s.elapsed_time(e))

    fb_times.sort(); fwd_times.sort()
    # use medians to suppress autotune-retuning spikes
    fb_med = fb_times[len(fb_times)//2]
    fwd_med = fwd_times[len(fwd_times)//2]
    bwd_med = fb_med - fwd_med
    print(f"[{label:8s}] fwd+bwd med = {fb_med:7.3f} ms | fwd med = {fwd_med:7.3f} ms | "
          f"bwd med = {bwd_med:7.3f} ms")
    return bwd_med


bwd_orig = bench(ORIGINAL_KERNEL, "orig")
bwd_patched = bench(PATCHED_KERNEL, "patched")

delta = bwd_orig - bwd_patched
speedup_pct = (delta / bwd_orig) * 100.0 if bwd_orig > 0 else 0.0
print(f"\nbwd delta = {delta:+.3f} ms  ({speedup_pct:+.2f}%)")
print("(template notes Triton CSE already fuses these dots, so near-zero "
      "delta on recent Triton is expected; patch value is in code clarity "
      "and robustness to older Triton / other arches.)")

sys.exit(0)
