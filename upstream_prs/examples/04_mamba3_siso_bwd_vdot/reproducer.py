# The patch text below is the same diff described in the PR template.  We
# operate on the *source* of the installed module (not the kernel object) so we
# can preserve autotune decorators and helper imports exactly.
_ORIG_BLOCK = '''        # ============================================================
        # Compute dADT Gradient (Part 1): From Intra-chunk Attention
        # This is register-heavy so we compute it early before spilling
        # ============================================================
        # Gradient contribution from (QK^T ⊙ L) V term
        dAinv = tl.dot(v_block, tl.trans(do_block))  # V @ dO^T
        if RECOMPUTE_MASK:
            dAinv *= tl.math.exp2(tl.minimum(da_cs[None, :] - da_cs[:, None], 0.0))
            dAinv = tl.where(
                tl.arange(0, CHUNK_SIZE)[None, :] > tl.arange(0, CHUNK_SIZE)[:, None],
                dAinv,
                0.0
            )
        else:
            dAinv *= causal_decay_mask
        dAinv *= tl.dot(k_block, tl.trans(q_block))  # Element-wise with K @ Q^T
        dM_rev_vector = tl.sum(dAinv, axis=0) - tl.sum(dAinv, axis=1)  # (CHUNK_SIZE,)

        # ============================================================
        # Compute dK: Key Gradient
        # dK = (V @ dO^T ⊙ mask)^T @ Q + V @ dStates * scale
        # ============================================================
        # Intra-chunk: dP^T @ Q where dP = dO @ V^T ⊙ mask
        dp_t_block = tl.dot(v_block, tl.trans(do_block))  # V @ dO^T: (CHUNK_SIZE, CHUNK_SIZE)
        if RECOMPUTE_MASK:
            dp_t_block *= tl.math.exp2(tl.minimum(da_cs[None, :] - da_cs[:, None], 0.0))
            dp_t_block = tl.where(
                tl.arange(0, CHUNK_SIZE)[None, :] > tl.arange(0, CHUNK_SIZE)[:, None],
                dp_t_block,
                0.0
            )
        else:
            dp_t_block *= causal_decay_mask

        acc_dk = tl.dot(dp_t_block.to(q_block.dtype), q_block)  # (CHUNK_SIZE, HEADDIM_QK)'''

_PATCHED_BLOCK = '''        # ============================================================
        # Compute V @ dO^T Once (shared by dADT, dK, dQ)  -- PR #04 patched
        # ============================================================
        vdot_block = tl.dot(v_block, tl.trans(do_block))  # V @ dO^T: (CHUNK_SIZE, CHUNK_SIZE)

        if RECOMPUTE_MASK:
            vdot_masked = vdot_block * tl.math.exp2(tl.minimum(da_cs[None, :] - da_cs[:, None], 0.0))
            vdot_masked = tl.where(
                tl.arange(0, CHUNK_SIZE)[None, :] > tl.arange(0, CHUNK_SIZE)[:, None],
                vdot_masked,
                0.0
            )
        else:
            vdot_masked = vdot_block * causal_decay_mask

        # ============================================================
        # Compute dADT Gradient (Part 1): From Intra-chunk Attention
        # ============================================================
        dAinv = vdot_masked * tl.dot(k_block, tl.trans(q_block))
        dM_rev_vector = tl.sum(dAinv, axis=0) - tl.sum(dAinv, axis=1)  # (CHUNK_SIZE,)

        # ============================================================
        # Compute dK: Key Gradient
        # dK = (V @ dO^T ⊙ mask)^T @ Q + V @ dStates * scale
        # ============================================================
        acc_dk = tl.dot(vdot_masked.to(q_block.dtype), q_block)  # (CHUNK_SIZE, HEADDIM_QK)'''

_ORIG_DQ = '''        # Intra-chunk: S^T @ K where S = V @ dO^T ⊙ mask
        s_block = tl.dot(v_block, tl.trans(do_block))  # (CHUNK_SIZE, CHUNK_SIZE)
        if RECOMPUTE_MASK:
            s_block *= tl.math.exp2(tl.minimum(da_cs[None, :] - da_cs[:, None], 0.0))
            s_block = tl.where(
                tl.arange(0, CHUNK_SIZE)[None, :] > tl.arange(0, CHUNK_SIZE)[:, None],
                s_block,
                0.0
            )
        else:
            s_block *= causal_decay_mask

        acc_dq = tl.dot(tl.trans(s_block).to(k_block.dtype), k_block)  # (CHUNK_SIZE, HEADDIM_QK)'''

_PATCHED_DQ = '''        # Intra-chunk: S^T @ K where S = V @ dO^T ⊙ mask  -- reuses vdot_masked from PR #04
        acc_dq = tl.dot(tl.trans(vdot_masked).to(k_block.dtype), k_block)  # (CHUNK_SIZE, HEADDIM_QK)'''


def build_patched_kernel():
    """Return a fresh Triton JIT kernel object with the vdot fusion applied.

    Triton's JIT re-parses kernel source via `inspect.getsource` at compile
    time, so the patched source must live in a real file on disk.  We copy the
    upstream module text to a temp file under a unique package name, apply the
    two substitutions, then import it and hand back its kernel.
    """
    import pathlib, tempfile, importlib.util

    src = inspect.getsource(siso_bwd_mod)
    if _ORIG_BLOCK not in src:
        raise RuntimeError(
            "Could not locate the original dADT/dK block in mamba3_siso_bwd.py -- "
            "the upstream source has drifted; update _ORIG_BLOCK in reproducer.py."
        )
    if _ORIG_DQ not in src:
        raise RuntimeError(
            "Could not locate the original dQ block in mamba3_siso_bwd.py -- "
            "the upstream source has drifted; update _ORIG_DQ in reproducer.py."
        )
    patched_src = src.replace(_ORIG_BLOCK, _PATCHED_BLOCK).replace(_ORIG_DQ, _PATCHED_DQ)

    assert patched_src.count("tl.dot(v_block, tl.trans(do_block))") == 1, (
        "Patched source should have exactly one V@dO^T dot; got "
        f"{patched_src.count('tl.dot(v_block, tl.trans(do_block))')}"
    )

    # Write under a unique filename so Triton's cache doesn't alias the orig
    tmpdir = pathlib.Path(tempfile.mkdtemp(prefix="pr04_vdot_"))
    patched_path = tmpdir / "mamba3_siso_bwd_patched.py"
    patched_path.write_text(patched_src)

    spec = importlib.util.spec_from_file_location(
        "mamba3_siso_bwd_patched_pr04", str(patched_path)
    )
    mod = importlib.util.module_from_spec(spec)
    sys.modules[spec.name] = mod
    spec.loader.exec_module(mod)
    return mod.mamba3_siso_bwd_kernel_dqkv


PATCHED_KERNEL = build_patched_kernel()
print("[build] patched kernel compiled OK "
      f"(orig id={id(ORIGINAL_KERNEL):#x}, patched id={id(PATCHED_KERNEL):#x})")


def install_kernel(kernel):
    """Swap the kernel referenced by compute_dqkv inside the bwd module."""
    siso_bwd_mod.mamba3_siso_bwd_kernel_dqkv = kernel


# ------------------------------------------------------------------
# Input generation (mirrors state-spaces/mamba tests/ops/triton/test_mamba3_siso.py)
# ------------------------------------------------------------------
def make_inputs(requires_grad: bool):
    torch.manual_seed(args.seed)
    torch.cuda.manual_seed_all(args.seed)

    B, S = args.batch, args.seqlen
    NH, NHQK = args.nheads, args.nheads_qk
    DQK, DV = args.headdim_qk, args.headdim_v

    Q = F.rms_norm(torch.randn(B, S, NHQK, DQK, device=device, dtype=DTYPE),
                   normalized_shape=(DQK,)).clone()
    K = F.rms_norm(torch.randn(B, S, NHQK, DQK, device=device, dtype=DTYPE),
                   normalized_shape=(DQK,)).clone()
    V = torch.randn(B, S, NH, DV, device=device, dtype=DTYPE)

    dt_max, dt_min = 0.1, 0.001
    a_init = -torch.empty(B, NH, S, device=device, dtype=torch.float32).uniform_(1.0, 16.0)
    dt = torch.exp(
        torch.rand(B, NH, S, device=device, dtype=torch.float32)
        * (math.log(dt_max) - math.log(dt_min)) + math.log(dt_min)
    )
    ADT = (a_init * dt).contiguous()
    DT = dt.contiguous()
    Trap = torch.empty(B, NH, S, device=device, dtype=DTYPE).uniform_(0.0, 1.0).clone()
    Q_bias = torch.randn(NH, DQK, device=device, dtype=DTYPE)
    K_bias = torch.randn(NH, DQK, device=device, dtype=DTYPE)

    headdim_angles = DQK // 4
    Angles = torch.randn(B, S, NH, headdim_angles, device=device, dtype=torch.float32)

    D = torch.ones((NH,), device=device, dtype=torch.float32) if not args.no_D else None
    Z = torch.randn(B, S, NH, DV, device=device, dtype=DTYPE) if not args.no_Z else None

    tensors = dict(Q=Q, K=K, V=V, ADT=ADT, DT=DT, Trap=Trap,
                   Q_bias=Q_bias, K_bias=K_bias, Angles=Angles,
                   D=D, Z=Z)
    if requires_grad:
        for name, t in tensors.items():
            if t is not None and t.is_floating_point():
                t.requires_grad_(True)

    # Fixed grad_out shared by both variants
    torch.manual_seed(args.seed + 1)
    grad_out = torch.randn(B, S, NH, DV, device=device, dtype=DTYPE)
    return tensors, grad_out


def forward_backward(tensors, grad_out):
    out = mamba3_siso_combined(
        tensors["Q"], tensors["K"], tensors["V"],
        tensors["ADT"], tensors["DT"], tensors["Trap"],
        tensors["Q_bias"], tensors["K_bias"], tensors["Angles"],
        D=tensors["D"], Z=tensors["Z"],
        Input_States=None,
        chunk_size=args.chunk_size,
        return_final_states=False,
        cu_seqlens=None,
    )
    # Zero existing grads
    for t in tensors.values():
        if isinstance(t, torch.Tensor) and t.grad is not None:
            t.grad = None
    out.backward(grad_out)
    grads = {}
    for name, t in tensors.items():
        if isinstance(t, torch.Tensor) and t.requires_grad:
            grads[name] = t.grad.detach().clone()
    return out.detach().clone(), grads


