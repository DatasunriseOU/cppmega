"""Reproducer for PR: Eliminate redundant V @ dO^T dot product in SISO backward kernel.

Template: upstream_prs/04_mamba3_siso_bwd_eliminate_redundant_vdot.md
Target:   state-spaces/mamba -> mamba_ssm/ops/triton/mamba3/mamba3_siso_bwd.py

What this script does
----------------------
1.  Loads mamba_ssm.ops.triton.mamba3.mamba3_siso_bwd unmodified (the upstream
    code which runs `tl.dot(v_block, tl.trans(do_block))` three times in the
    inner chunk loop of `mamba3_siso_bwd_kernel_dqkv`).
2.  Replaces that kernel at runtime with a hand-patched version that computes
    `V @ dO^T` once and reuses the masked result for dADT, dK and dQ.
3.  Runs `mamba3_siso_combined` forward + backward with both variants on the
    same RNG state and compares every gradient tensor (Q, K, V, ADT, DT, Trap,
    Q_bias, K_bias, Angles, D, Z).  Exits non-zero on any mismatch above
    `REL_TOL`.
4.  Times the backward call for both variants (cuda events, warmup + N iters)
    and prints the timing delta.

The optimized kernel is generated by AST-editing the bwd module's source, then
exec'd in a clean namespace that mirrors the module's imports.  This keeps the
reproducer fully self-contained - nothing is written to the installed package.
"""
