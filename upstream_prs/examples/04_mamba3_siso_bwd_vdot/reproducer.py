out_new, grads_new = forward_backward(tensors_new, grad_out2)
torch.cuda.synchronize()

# Forward should be identical (this PR only touches bwd)
out_max_abs = (out_orig.float() - out_new.float()).abs().max().item()
print(f"fwd max |orig - patched| = {out_max_abs:.3e}")
if out_max_abs > 0:
    print(f"WARNING: forward differs by {out_max_abs:.3e}; PR only edits bwd, expected 0")

fail = False
for name in sorted(grads_orig):
    a = grads_orig[name].float()
    b = grads_new[name].float()
    diff = (a - b).abs()
    denom = a.abs().clamp_min(1e-6)
    rel = (diff / denom).max().item()
    absmax = diff.max().item()
    status = "OK" if (rel < args.rel_tol and absmax < args.abs_tol) else "FAIL"
    if status == "FAIL":
        fail = True
    print(f"  grad[{name:8s}]  abs_max={absmax:.3e}  rel_max={rel:.3e}  [{status}]")

if fail:
    print("CORRECTNESS FAIL - patched kernel gradients diverge beyond tolerance")
    sys.exit(1)
print("CORRECTNESS OK")


